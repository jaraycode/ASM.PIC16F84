		LIST P=16F84A

STATUS	EQU	03H
PUERTOA	EQU	05H
PUERTOB	EQU	06H
TRISA	EQU	05H
TRISB	EQU	06H

	ORG	0
	
	BSF		STATUS,5
	CLRF	TRISB
	MOVLW	B'00011111'
	MOVWF	TRISA
	BCF		STATUS,5
	
INICIO:	BTFSC	PUERTOA,4
		GOTO	CUATRO
		BTFSC	PUERTOA,3
		GOTO	TRES
		BTFSC	PUERTOA,2
		GOTO	DOS
		BTFSC	PUERTOA,1
		GOTO	UNO
		BTFSC	PUERTOA,0
		GOTO	CERO

		MOVLW	B'11011111'
		MOVWF	PUERTOB
		GOTO	INICIO
			
			
CUATRO:	MOVLW	B'10011001' ; ESTE SERA LA U EN UCAB
		MOVWF	PUERTOB
		GOTO	INICIO
			
			
TRES:	MOVLW	B'10110000' ; ESTE SERA LA C EN UCAB
		MOVWF	PUERTOB
		GOTO	INICIO
			
			
DOS:	MOVLW	B'10100100' ; ESTE SERA LA A EN UCAB
		MOVWF	PUERTOB
		GOTO	INICIO
			
			
UNO:	MOVLW	B'11111001' ; ESTE SERA LA b EN UCAB
		MOVWF	PUERTOB
		GOTO	INICIO
			
		
CERO:	MOVLW	B'11000000' ; ESTE SERA CUALQUIER CARACTER
		MOVWF	PUERTOB
		GOTO	INICIO
		END