		LIST P=16F84
		
		
STATUS		EQU		03H ; INICIANDO ESTADO
PUERTOB		EQU 	06H
TRISB		EQU		06H
PUERTOA		EQU		05H
TRISA		EQU		05H


		ORG		0		; INICIO DE PROGRAMA
		
		BSF		STATUS,5	; BANCO DE MEMORIA 1
		CLRF	TRISB 		; INICIALIZAR PARA SALIDA
		MOVLW 	H'FF'
		MOVWF	TRISA		; INICIALIZAR PARA ENTRADA
		BCF		STATUS,5	; BANCO DE MEMORIA 0
		
INICIO	MOVF	PUERTOA,0		; VALOR DE PA A W
		ANDLW 	B'00011111'
		SUBLW	B'00001011'
		
		BTFSS	STATUS,2	; BANDERA Z (SI W=0, Z=1 Y VICEVERSA)
		GOTO	ERRO
		MOVLW	B'10100011'	; IMPRIMIR O, SEGMENTOS PRENDIDOS C,D,E,G
A1		MOVWF	PUERTOB
		GOTO	INICIO
		
ERRO	MOVLW	B'01000110'	; IMPRIMIR C, SEGMENTOS PRENDIDOS A,D,E,F
		GOTO A1

		END